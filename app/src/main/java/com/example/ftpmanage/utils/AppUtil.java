    /**
     * 检查指定的文件名后缀是否匹配
     *
     * @param s    文件名
     * @param exts 文件名后缀集合，多个后缀使用小写逗号分隔， 例如：jpg,jpeg,bmp
     * @return
     */
    public static boolean isExt(String s, String exts) {
        if (s.equals("")) {
            return false;
        }
        String[] ex = exts.split(",");
        for (int i = 0; i < ex.length; i++) {
            String exs = ex[i].toLowerCase();
            if (!exs.equals("")) {
                exs = "." + exs;
                if (s.toLowerCase().endsWith(exs)) {
                    return true;
                }
            }
        }
        return false;
    }
